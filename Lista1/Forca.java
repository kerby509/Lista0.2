package Lista1;

public class Forca {
    private int a;
    private int b;


    public int getA(){
        return this.a;
    }
    
    public void setA(int a){
        this.a = a;
    }

    public int getB(){
        return this.b;
    }

    public void setB(int b){
        this.b = b;
    }

    public double tamanho(){
        return this.a * this.b;
    }
}
